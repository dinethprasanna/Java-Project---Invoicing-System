    //this method for load the customer table data inside gui table
    public void customerTableLoad() throws ClassNotFoundException,SQLException{
    
        ResultSet rs=CustomerController.getCustomers();
        manageCustomerTable.setModel(DbUtils.resultSetToTableModel(rs));
    }
    
        //this method for set the table data to text boxes
    public void setCustomerTableData(){
    
        int r =manageCustomerTable.getSelectedRow();
        
        String cusID=manageCustomerTable.getValueAt(r,0).toString();
        String cusName=manageCustomerTable.getValueAt(r,1).toString();
        String eMail=manageCustomerTable.getValueAt(r,2).toString();
        String address=manageCustomerTable.getValueAt(r,3).toString();
        String telephone=manageCustomerTable.getValueAt(r,4).toString();
        String DOB=manageCustomerTable.getValueAt(r,5).toString();
        String gender=manageCustomerTable.getValueAt(r,6).toString();
        
        customerIDBox.setText(cusID);
        customerNameBox.setText(cusName);
        customerEmailBox.setText(eMail);
        customerAddressBox.setText(address);
        customerTelephoneBox.setText(telephone);
        customerDOBBox.setText(DOB);
        customerGenderBox.setText(gender);
        
    }
    
    //this for clear all texboxes
    public void clearFields(){
        
        customerIDBox.setText("");
        customerNameBox.setText("");
        customerEmailBox.setText("");
        customerAddressBox.setText("");
        customerTelephoneBox.setText("");
        customerDOBBox.setText("YYYY-MM-DD");
        customerGenderBox.setText("");
    }
    
    //this for search mechanism
    public void searchCustomer() throws ClassNotFoundException,SQLException{
    
        String srch=searchCustomerBox.getText();
        ResultSet rs=CustomerController.customerSearch(srch);
        manageCustomerTable.setModel(DbUtils.resultSetToTableModel(rs));
        
    }
    
