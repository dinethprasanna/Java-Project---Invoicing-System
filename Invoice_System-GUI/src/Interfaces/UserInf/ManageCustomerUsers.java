    private void addCustomerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCustomerButtonActionPerformed
        
        try {
            Customer c1=new Customer();
            c1.setCustomerID(customerIDBox.getText());
            c1.setCustomerName(customerNameBox.getText());
            c1.setEmail(customerEmailBox.getText());
            c1.setAddress(customerAddressBox.getText());
            c1.setContactNumber(Integer.parseInt(customerTelephoneBox.getText()));
            c1.setDateOfBirth(customerDOBBox.getText());
            c1.setGender(customerGenderBox.getText());
            
            int i=CustomerController.addCustomer(c1);
            if(i!=0){
                JOptionPane.showMessageDialog(null,"Customer Added");
                customerTableLoad();
                clearFields();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ManageCustomerAdmins.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ManageCustomerAdmins.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_addCustomerButtonActionPerformed

